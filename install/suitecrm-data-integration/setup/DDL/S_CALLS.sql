DROP TABLE IF EXISTS S_CALLS;
CREATE TABLE S_CALLS (
    id VARCHAR(36) DEFAULT NULL,
    name VARCHAR(50) DEFAULT NULL,
    date_entered DATETIME DEFAULT NULL,
    date_modified DATETIME DEFAULT NULL,
    modified_user_id VARCHAR(36) DEFAULT NULL,
    created_by VARCHAR(36) DEFAULT NULL,
    description TEXT DEFAULT NULL,
    deleted BOOLEAN DEFAULT NULL,
    assigned_user_id VARCHAR(36) DEFAULT NULL,
    duration_hours INT DEFAULT NULL,
    duration_minutes INT DEFAULT NULL,
    date_start DATETIME DEFAULT NULL,
    date_end DATETIME DEFAULT NULL,
    parent_type VARCHAR(255) DEFAULT NULL,
    status VARCHAR(100) DEFAULT NULL,
    direction VARCHAR(100) DEFAULT NULL,
    parent_id VARCHAR(36) DEFAULT NULL,
    reminder_time INT DEFAULT NULL,
    email_reminder_time INT DEFAULT NULL,
    email_reminder_sent BOOLEAN DEFAULT NULL,
    outlook_id VARCHAR(255) DEFAULT NULL,
    repeat_type VARCHAR(36) DEFAULT NULL,
    repeat_interval INT DEFAULT NULL,
    repeat_dow VARCHAR(7) DEFAULT NULL,
    repeat_until DATETIME DEFAULT NULL,
    repeat_count INT DEFAULT NULL,
    repeat_parent_id VARCHAR(36) DEFAULT NULL,
    recurring_source VARCHAR(36) DEFAULT NULL
);