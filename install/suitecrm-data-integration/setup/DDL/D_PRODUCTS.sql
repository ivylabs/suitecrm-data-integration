DROP TABLE IF EXISTS `D_PRODUCTS`;
CREATE TABLE `D_PRODUCTS` (
  `PRODUCT_TK` BIGINT(20) NOT NULL,
  `VERSION` INT(11) DEFAULT NULL,
  `DATE_FROM` DATETIME DEFAULT NULL,
  `DATE_TO` DATETIME DEFAULT NULL,
  `PRODUCT_ID` VARCHAR(36) DEFAULT NULL,
  `PRODUCT_NAME` VARCHAR(255) DEFAULT NULL,
  `PRODUCT_DESCRIPTION` TEXT,
  `PRODUCT_DELETED` VARCHAR(3) DEFAULT NULL,
  `PRODUCT_MAINCODE` VARCHAR(100) DEFAULT NULL,
  `PRODUCT_PART_NUMBER` VARCHAR(25) DEFAULT NULL,
  `PRODUCT_TYPE` VARCHAR(100) DEFAULT NULL,
  `PRODUCT_UNIT_COST` DECIMAL(26,6) DEFAULT NULL,
  `PRODUCT_CURRENCY_ID` VARCHAR(36) DEFAULT NULL,
  `PRODUCT_UNIT_PRICE` DECIMAL(26,6) DEFAULT NULL,
  `PRODUCT_URL` VARCHAR(255) DEFAULT NULL,
  `PRODUCT_IMAGE` VARCHAR(255) DEFAULT NULL,
  `PRODUCT_CATEGORY_ID` VARCHAR(36) DEFAULT NULL,
  `PRODUCT_CATEGORY_NAME` VARCHAR(255) DEFAULT NULL,
  `PRODUCT_CATEGORY_DESCRIPTION` TEXT,
  `PRODUCT_CATEGORY_DELETED` CHAR(1) DEFAULT NULL,
  PRIMARY KEY (`PRODUCT_TK`),
  KEY `IDX_D_PRODUCTS_LKP` (`PRODUCT_ID`),
  KEY `IDX_D_PRODUCTS_TK` (`PRODUCT_TK`)
);