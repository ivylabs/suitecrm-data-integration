DROP TABLE IF EXISTS `D_ACCOUNTS`;
CREATE TABLE `D_ACCOUNTS` (
  `ACCOUNT_TK` BIGINT(20) NOT NULL,
  `VERSION` INT(11) DEFAULT NULL,
  `DATE_FROM` DATETIME DEFAULT NULL,
  `DATE_TO` DATETIME DEFAULT NULL,
  `ACCOUNT_ID` VARCHAR(36) DEFAULT NULL,
  `ACCOUNT_NAME` VARCHAR(150) DEFAULT NULL,
  `ACCOUNT_DATE_CREATED` DATETIME DEFAULT NULL,
  `ACCOUNT_MODIFIED` DATETIME DEFAULT NULL,
  `ACCOUNT_CREATED_BY` VARCHAR(36) DEFAULT NULL,
  `ACCOUNT_DELETED` VARCHAR(3) DEFAULT NULL,
  `ACCOUNT_DESCRIPTION` TEXT,
  `ACCOUNT_TYPE` VARCHAR(50) DEFAULT NULL,
  `ACCOUNT_INDUSTRY` VARCHAR(50) DEFAULT NULL,
  `ACCOUNT_BILLING_ADDRESS_CITY` VARCHAR(100) DEFAULT NULL,
  `ACCOUNT_BILLING_ADDRESS_COUNTRY` VARCHAR(255) DEFAULT NULL,
  `ACCOUNT_BILLING_ADDRESS_POSTCODE` VARCHAR(20) DEFAULT NULL,
  `ACCOUNT_BILLING_ADDRESS_STATE` VARCHAR(100) DEFAULT NULL,
  `ACCOUNT_BILLING_ADDRESS_STREET` VARCHAR(150) DEFAULT NULL,
  `ACCOUNT_SHIPPING_ADDRESS_CITY` VARCHAR(100) DEFAULT NULL,
  `ACCOUNT_SHIPPING_ADDRESS_COUNTRY` VARCHAR(255) DEFAULT NULL,
  `ACCOUNT_SHIPPING_ADDRESS_POSTCODE` VARCHAR(20) DEFAULT NULL,
  `ACCOUNT_SHIPPING_ADDRESS_STATE` VARCHAR(100) DEFAULT NULL,
  `ACCOUNT_SHIPPING_ADDRESS_STREET` VARCHAR(150) DEFAULT NULL,
  PRIMARY KEY (`ACCOUNT_TK`),
  KEY `IDX_D_ACCOUNTS_LKP` (`ACCOUNT_ID`),
  KEY `IDX_D_ACCOUNTS_TK` (`ACCOUNT_TK`)
);