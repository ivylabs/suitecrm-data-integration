DROP TABLE IF EXISTS `D_USERS`;
CREATE TABLE `D_USERS` (
  `USER_TK` BIGINT(20) NOT NULL,
  `VERSION` INT(11) DEFAULT NULL,
  `DATE_FROM` DATETIME DEFAULT NULL,
  `DATE_TO` DATETIME DEFAULT NULL,
  `USER_ID` VARCHAR(36) DEFAULT NULL,
  `USER_NAME` VARCHAR(60) DEFAULT NULL,
  `USER_FIRST_NAME` VARCHAR(255) DEFAULT NULL,
  `USER_LAST_NAME` VARCHAR(255) DEFAULT NULL,
  `USER_FULL_NAME` TINYTEXT DEFAULT NULL,
  `USER_DESCRIPTION` TEXT,
  `USER_TITLE` VARCHAR(50) DEFAULT NULL,
  `USER_DEPARTMENT` VARCHAR(50) DEFAULT NULL,
  `USER_PHONE_HOME` VARCHAR(50) DEFAULT NULL,
  `USER_PHONE_MOBILE` VARCHAR(50) DEFAULT NULL,
  `USER_PHONE_WORK` VARCHAR(50) DEFAULT NULL,
  `USER_PHONE_OTHER` VARCHAR(50) DEFAULT NULL,
  `USER_STATUS` VARCHAR(100) DEFAULT NULL,
  `USER_ADDRESS_STREET` VARCHAR(150) DEFAULT NULL,
  `USER_ADDRESS_CITY` VARCHAR(100) DEFAULT NULL,
  `USER_ADDRESS_STATE` VARCHAR(100) DEFAULT NULL,
  `USER_ADDRESS_COUNTRY` VARCHAR(100) DEFAULT NULL,
  `USER_ADDRESS_POSTCODE` VARCHAR(20) DEFAULT NULL,
  PRIMARY KEY (`USER_TK`),
  KEY `IDX_D_USERS_LKP` (`USER_ID`),
  KEY `IDX_D_USERS_TK` (`USER_TK`)
);
