DROP TABLE IF EXISTS `D_LEADS`;
CREATE TABLE `D_LEADS` (
  `LEAD_TK` BIGINT(20) NOT NULL,
  `VERSION` INT(11) DEFAULT NULL,
  `DATE_FROM` DATETIME DEFAULT NULL,
  `DATE_TO` DATETIME DEFAULT NULL,
  `LEAD_ID` VARCHAR(36) DEFAULT NULL,
  `LEAD_DESCRIPTION` TEXT,
  `LEAD_SALUTATION` VARCHAR(255) DEFAULT NULL,
  `LEAD_TITLE` VARCHAR(100) DEFAULT NULL,
  `LEAD_SOURCE` VARCHAR(100) DEFAULT NULL,
  `LEAD_SOURCE_DESCRIPTION` TEXT,
  `LEAD_STATUS` VARCHAR(100) DEFAULT NULL,
  `LEAD_STATUS_DESCRIPTION` TEXT,
  `LEAD_ACCOUNT_NAME` VARCHAR(255) DEFAULT NULL,
  `LEAD_ACCOUNT_DESCRIPTION` TEXT,
  `LEAD_OPPORTUNITY_NAME` VARCHAR(255) DEFAULT NULL,
  `LEAD_OPPORTUNITY_AMOUNT` VARCHAR(50) DEFAULT NULL,
  `LEAD_WEBSITE` VARCHAR(255) DEFAULT NULL,
  `LEAD_FULL_NAME` TINYTEXT,
  `LEAD_PRIMARY_ADDRESS_CITY` VARCHAR(100) DEFAULT NULL,
  `LEAD_PRIMARY_ADDRESS_COUNTRY` VARCHAR(255) DEFAULT NULL,
  `LEAD_PRIMARY_ADDRESS_STATE` VARCHAR(100) DEFAULT NULL,
  `LEAD_PRIMARY_ADDRESS_POSTCODE` VARCHAR(20) DEFAULT NULL,
  `LEAD_PRIMARY_ADDRESS_STREET` VARCHAR(150) DEFAULT NULL,
  `LEAD_CONVERTED` VARCHAR(24) DEFAULT NULL,
  PRIMARY KEY (`LEAD_TK`),
  KEY `IDX_D_LEADS_LKP` (`LEAD_ID`),
  KEY `IDX_D_LEADS_TK` (`LEAD_TK`)
);
